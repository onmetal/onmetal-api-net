package controllers

import (
	"github.com/onmetal/onmetal-api-net/api/v1alpha1"
	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	. "sigs.k8s.io/controller-runtime/pkg/envtest/komega"
)

var _ = Describe("PartitionController", func() {
	It("should maintain a partition", func(ctx SpecContext) {
		By("waiting for the partition to be present")
		partition := &v1alpha1.Partition{
			ObjectMeta: metav1.ObjectMeta{
				Name: partitionName,
			},
		}
		Eventually(Get(partition)).Should(Succeed())
	})
})
